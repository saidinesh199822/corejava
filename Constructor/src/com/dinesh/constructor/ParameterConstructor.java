package com.dinesh.constructor;

public class ParameterConstructor {
	int price;
	String color;
	String display;
	double size;
	public ParameterConstructor(int price, String color, String display, double size) {
		super();
		this.price = price;
		this.color = color;
		this.display = display;
		this.size = size;
	}
	
	

}
